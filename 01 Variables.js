"use strict"; // To avoid and notify error

/* intro to variables:-
    • Variables are name or reference of memory locations, where we store data.
    • It is like a container, that stores a value.
    • variables can store some information
    • we can use that information later
    • we can change that information later
*/

// declare a variable 
var firstName = "Harshit";

// use a variable 
console.log(firstName); // console.log can print something on console 

// change value 
firstName = "Mohit";
console.log(firstName);
